public class PrimitiveDtSize {

	public static void main(String[] args) {
		// data type
		System.out.println("Primitive data types size");
		
		// primitive data type
		// Boolean // boolean
		// Character // char
		// Integer // byte, short, int, long
		// Floating // float, double
		
		
		// primitive-data type size
		// boolean = 1 bit // 8 bit = 1 byte
		// char = 2 bytes
		// byte = 1 byte
		// short = 2 bytes
		// int = 4 bytes
		// long = 8 bytes
		// float = 4 bytes
		// double = 8 bytes
		
		
		System.out.println("\nboolean size: 1 bit");
		System.out.println("char size: 2 bytes");
		System.out.println("byte size: 1 bytes");
		System.out.println("short size: 2 bytes");
		System.out.println("int size: 4 bytes");
		System.out.println("long size: 8 bytes");
		System.out.println("float size: 4 bytes");
		System.out.println("double size: 8 bytes");
		

		// Stores whole numbers from
		// boolean = 1 bit // Stores true or false values
		// char = 2 bytes // single character/letter or ASCII value
		// byte = 1 byte // -128 to 127
		// short = 2 bytes // -32,768 to 32,767
		// int = 4 bytes // -2,147,483,648 to 2,147,483,647
		// long = 8 bytes // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		// float = 4 bytes // Sufficient for storing 6 to 7 decimal digits
		// double = 8 bytes Sufficient for storing 15 decimal digits
		
	}
}
