
public class EscapeSequences {

	public static void main(String[] args) {
		// Escape sequences
		System.out.println("Escape sequences ");
		
		// \n
		System.out.println("\nMy name is Hasan\nMy Age is 21");
		System.out.println("\nI am a Software developer\nI am working now Google");
		
		// \r
		System.out.println("\nHello\rFriend");
		
		
		// \t
		System.out.println("\nHello\tWorld!");
		
		// \b
		System.out.println("Hel\blo Friends!");
		
		// \f
		System.out.println("Hello Micro\fsoft");
		

	}

}
