
public class ForLoop {

	public static void main(String[] args) {
		// For loop
		System.out.println("For loop in Java\n=====");
		
		// array
		String[] cars = {"Volvo", "BMW", "Ford", "Mazda\n"};
		
		// for loop
		for (String i : cars) {
		  System.out.println(i);
		}
		
		// name print
		for(int i = 1; i<=10; i++) {
			System.out.println("Console print: " + i);
		}
		

	}

}
