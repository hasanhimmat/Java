public class JavaMath {

	public static void main(String[] args) {
		// Java Math
		System.out.println("Java Math method\n");
		
		// max method
		int maxValue = Math.max(20, 18);
		System.out.println("Max value is: " + maxValue);
		
		// max method
		int minValue = Math.min(20, 18);
		System.out.println("Max value is: " + minValue);
	
		// direct output
		System.out.println(Math.sqrt(9));
		
		System.out.println(Math.abs(-18));
		
		// System.out.println(Math.random() * 100 - 1); // set this my js skill
		
		System.out.println(Math.random() * 101);
	}

}
