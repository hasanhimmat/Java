public class Arrays {
    public static void main(String[] args){
        // output
        System.out.println("===== Arrays of java =====");

        // arrays car list
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

        // arrays car price list
        int[] price = {100, 59, 87, 250};

        // output
        System.out.println(cars[1] + " " + price[0]);
        System.out.println(cars[2] + " " + price[1]);
        System.out.println(cars[0] + " " + price[2]);
        System.out.println(cars[3] + " " + price[3]);


        // array length
        System.out.println("Available product: " + cars.length);

    }
}
